        String matKhauMoi2 = txtNhapLaiMatKhauMoi.getText();
        if (matKhauMoi.equals("") || matKhauMoi2.equals("")) {
            MsgBox.AlertFall(this, "Vui lòng nhập đầy đủ!!");
        } else if (!PassHashing.isValidPassword(matKhauMoi)) {
            MsgBox.AlertFall(this, "Vui lòng nhập mật khẩu có ít nhất một số, một kí tự hoa, một kí tự thường!!");
        } else if (!matKhauMoi.equals(matKhauMoi2)) {
            MsgBox.AlertFall(this, "Mật khẩu mới chưa khớp");
        } else {
            try {
                TaiKhoan tk = dao.selectById(txtEmail.getText());
                tk.setMatKhau(PassHashing.hashPassword(matKhauMoi));
                dao.update(tk);
                MsgBox.AlertSuccess(this, "Cập nhật mật khẩu thành công");
                this.dispose();

            } catch (Exception e) {
                MsgBox.AlertFall(this, "Có lỗi xảy ra vui long quay lại sau!!!");
            }

        }
