    public Boolean kiemtraKhachHang() {
        if (Validate.nothingText(txtMaKhachHang)) {
            MsgBox.AlertFall(this, "Không để trống mã khách hàng");
            return false;
        } else if (Validate.nothingText(txtCCCD)) {
            MsgBox.AlertFall(this, "Không để trống Căn cước công dân");
            return false;
        } else if (Validate.nothingText(txtTenKhachHang)) {
            MsgBox.AlertFall(this, "Không để trống Tên khách hàng");
            return false;
        } else if (Validate.nothingText(cboGioiTinh)) {
            MsgBox.AlertFall(this, "Không để trống Giới tính");
            return false;
        } else if (Validate.nothingText(txtEmail)) {
            MsgBox.AlertFall(this, "Không để trống Email");
            return false;
        } else if (Validate.nothingText(txtSDT)) {
            MsgBox.AlertFall(this, "Không để trống Số điện thoại");
            return false;
        } else if (Validate.nothingText(txtTichDiem)) {
            MsgBox.AlertFall(this, "Không để trống tích điểm");
            return false;
        }
        return true;
    }

