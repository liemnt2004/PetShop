    void clear() {
        this.setModel(new HoiVien());
        this.setStatus(true);
    }

    void them() {
        HoiVien model = getModel();
        if (kiemtraKhachHang()) {
            try {
                hvdao.insert(model);
                this.load();
                this.clear();
                JOptionPane.showMessageDialog(this, "thêm thành công");
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "thêm thất bại");
            }
        }

    }

    void capnhat() {
        HoiVien model = getModel();
        if (kiemtraKhachHang()) {
            try {
                hvdao.update(model);
                this.load();
                this.clear();
                JOptionPane.showMessageDialog(this, "cập nhật thành công");
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "cập nhật thất bại");
            }
        }

    }

    void xoa() {
        int userChoice = JOptionPane.showConfirmDialog(this, "Bạn thực sự muốn xóa người học này?", "Xác nhận xóa", JOptionPane.YES_NO_OPTION);

        if (userChoice == JOptionPane.YES_OPTION) {
            String manh = txtMaKhachHang.getText();
            try {
                hvdao.delete(manh);
                this.load();
                this.clear();
                JOptionPane.showMessageDialog(this, "Xóa thành công!");
            } catch (HeadlessException e) {
                JOptionPane.showMessageDialog(this, "Xóa thất bại!");
            }
        }
    }

    void Moi() {
        this.clear();
    }

    void setModel(HoiVien model) {
        txtMaKhachHang.setText(model.getMaHoiVien());
        txtCCCD.setText(model.getCccd());
        txtTenKhachHang.setText(model.getTenKhachHang());
        txtEmail.setText(model.getEmail());
        txtSDT.setText(model.getSoDienThoai());
        cboGioiTinh.setText(model.getGioiTinh());
        txtTichDiem.setText(String.valueOf(model.getTichDiem()));
    }

    HoiVien getModel() {
        HoiVien model = new HoiVien();
        model.setMaHoiVien(txtMaKhachHang.getText());
        model.setCccd(txtCCCD.getText());
        model.setTenKhachHang(txtTenKhachHang.getText());
        model.setEmail(txtEmail.getText());
        model.setSoDienThoai(txtSDT.getText());
        model.setGioiTinh(cboGioiTinh.getText());
        return model;
    }

    void setStatus(boolean inserttable) {
        btnThem.setEnabled(inserttable);
        btnCapNhat.setEnabled(!inserttable);
        btnXoa.setEnabled(!inserttable);
    }

